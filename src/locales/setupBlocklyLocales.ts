export const setBlocklyLocale = async (lang: string) => {
    // 1. Set Standard Locale via Dynamic Import
    if (lang === 'zh' || lang === 'zh-CN') {
        const ZhHans = await import('blockly/msg/zh-hans');
        Blockly.setLocale(ZhHans as any);
    } else {
        const En = await import('blockly/msg/en');
        Blockly.setLocale(En as any);
    }

    // 2. Inject Custom Messages (注入自定义消息)

    // Strategy: Always load English keys first as fallback (Safeguard)
    // This ensures that if a key is missing in the target language (e.g. zh), 
    // it falls back to English instead of showing %{BKY_KEY}
    const enKeys = CustomMsgs.en;
    for (const key in enKeys) {
        // @ts-ignore
        Blockly.Msg[key] = enKeys[key];
    }

    // Then overwrite with Target Language if different
    if (lang === 'zh' || lang === 'zh-CN') {
        const target = CustomMsgs.zh;
        console.log(`[Locale] Overwriting with ${Object.keys(target).length} Chinese keys.`);
        for (const key in target) {
            // @ts-ignore
            Blockly.Msg[key] = target[key];
        }
    }
};
